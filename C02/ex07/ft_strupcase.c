char	*ft_strupcase(char *str)
{
	char	*s;

	s = str;
	if (!str || !*str)
		return (str);
	while (*s)
	{
		*s = *s - ('a' <= *s && *s <= 'z') * ('a' - 'A');
		s++;
	}
	return (str);
}
